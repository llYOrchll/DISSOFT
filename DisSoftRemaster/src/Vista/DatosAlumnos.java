    public void AutoCarga(){
        List<alumnos> listaAlumnos = new ArrayList<>();
        listaAlumnos = LeerCSV.ImpAlumnosCSV();
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido Paterno");
        modelo.addColumn("Apellido Materno");
        modelo.addColumn("Matricula");
        
        this.jTable1.setModel(modelo);
        
        String data[][] = new String[listaAlumnos.size()][4];
        for(int i=0; i< listaAlumnos.size(); i++){
            data[i][0] = listaAlumnos.get(i).getNombre();
            data[i][1] = listaAlumnos.get(i).getApellidoP();
            data[i][2] = listaAlumnos.get(i).getApellidoM();
            data[i][3] = listaAlumnos.get(i).getMatricula();
        }
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[] {
                    "Nombre", "Apellido Paterno", "Apellido Materno", "Matricula"
                }
        ));
    }
