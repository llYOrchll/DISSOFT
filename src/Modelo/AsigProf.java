public class AsigProf {
    String claveM;
    String NomAsig;

    public AsigProf(String claveM, String NomAsig) {
        this.claveM = claveM;
        this.NomAsig = NomAsig;
    }

    public String getClaveM() {
        return claveM;
    }

    public String getNomAsig() {
        return NomAsig;
    }

    public void setClaveM(String claveM) {
        this.claveM = claveM;
    }

    public void setNomAsig(String NomAsig) {
        this.NomAsig = NomAsig;
    }

    
    
    
    
}
